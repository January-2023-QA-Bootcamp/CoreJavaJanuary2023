package lec02_java_basic;

// Memorize it please
public class H_must_learn { // Starting of the class body
	// This is called a Class Body
	// What a class body contain?
	// First: Class body contain variables
	// Those variables can be either declared (no value assigned) or initialized (value assigned) 
	
	public String myName; // variable declared
	// here, public is access modifier, String is the variable type, myName is the name of the variable
	public String familyName = "Sharkar"; // variable initialized 
	// here, public is access modifier, String is the variable type, familyName is the name of the variable
	// String type valu are always inside double quotation
	public int age = 100;
	public char sex = 'M';
	public boolean usCitizen = true;

	// Second: Class body also contains Constructor
	// Those Constructor can be either declared or initialized
	
	// Third: Class body also contains methods
	// Those methods can be either implemented or initialized. (very important info)
	// Inside the class, method can't be declared  (very important info)

} // Ending of the class body
