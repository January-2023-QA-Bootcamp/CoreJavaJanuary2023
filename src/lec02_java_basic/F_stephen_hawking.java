package lec02_java_basic;

// some symbol and characters are used inside String
// use of \n --> creating new line  ---> a lot of use of \n in IT industry
// use of \t --> creating a tab  ---> a lot of use of \t
// Below 2 is: extra information, not used generally
// use of \b --> creating back space -- not used --- also doesn't work in eclipse (Work in Intellij)
// use of \r --> creating carriage return -- not used -- similar like \n

public class F_stephen_hawking {

	public static void main(String[] args) {
		System.out.println("Stephen Hawking was an English theoretical physicist and cosmologist, who despite being afflicted motor neurone disease that severely limited his physical abilities, was able to build a phenomenally successful career.");
		System.out.println("\n-------------------------------------------------------------------------------------------------\n");
		System.out.println("\tStephen Hawking was an English theoretical physicist and cosmologist, \n\twho despite being afflicted motor neurone disease that severely limited his physical abilities, \n\twas able to build a phenomenally successful career.");
	}
}
