package lec02_java_basic;

// use of " " inside String---> \"  \" 
// use of ' ' inside String ---> \'  \'
// use of \ \  inside String---> \\   \\

public class G_use_of_single_or_double_quotation_inside_a_String {

	public static void main(String[] args) {
		System.out.println("Stephen Hawking was an English theoretical physicist and cosmologist, who despite being afflicted motor neurone disease that severely limited his physical abilities, was able to build a phenomenally successful career.");
		System.out.println("\n-------------------------------------------------------------------------------------------------\n");
		System.out.println("\t\"Stephen Hawking\" was an \'English theoretical physicist and cosmologist\', \n\twho despite being afflicted \\motor neurone disease\\ that severely limited his physical abilities, \n\twas able to build a phenomenally successful career.");
	}
}
